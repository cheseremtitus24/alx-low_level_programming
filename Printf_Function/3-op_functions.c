#include "main.h"

/**
 * op_add- Adds two numbers
 * @a: first number
 * @b: second number
 *
 * Description: performs addition operation
 * Return: a digit
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- subtracts b from a
 * @a: first number
 * @b: second number
 *
 * Description: performs difference operation
 * Return: a digit
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multiplies two integer numbers
 * @a: first number
 * @b: second number
 *
 * Description: performs multiplication operation
 * Return: a digit
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- divides a numerator by a denominator
 * @a: first number
 * @b: second number
 *
 * Description: performs division operation
 * Return: a digit
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- returns remainder of a division of two numbers
 * @a: first number
 * @b: second number
 *
 * Description: performs modulus operation
 * Return: a digit
 */
int op_mod(int a, int b)
{
	return (a % b);
}

